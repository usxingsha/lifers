# 组合系统提示词 (Composite System Prompts)
# ===========================================================================

def build_system_prompt(
    mode: str = "chat",
    include_rules: bool = True,
    include_tools: bool = False,
    include_safety: bool = True,
    include_self_awareness: bool = True,
    compact: bool = False,
) -> str:
    """构建完整的系统提示词。

    Args:
        mode: 对话模式 (chat/code/think/creative/analysis/teach)
        include_rules: 是否包含行为准则
        include_tools: 是否包含工具指引
        include_safety: 是否包含安全边界
        include_self_awareness: 是否包含自我认知
        compact: 紧凑模式（减少输出长度，适合小上下文窗口）

    Returns:
        组合后的系统提示词字符串
    """
    parts = [LIFERS_IDENTITY_ZH]

    if not compact:
        # 能力概述
        parts.append("\n【核心能力】")
        for cap in CAPABILITIES_CATEGORIES:
            parts.append(f"- {cap}")

    if include_rules and not compact:
        parts.append("\n【行为准则】")
        for rule in BEHAVIOR_RULES:
            parts.append(f"- {rule}")
    elif include_rules and compact:
        parts.append("\n【准则】诚实准确、简洁优先、中文为主、高风险须确认。")

    if include_tools:
        parts.append("\n【工具使用】")
        for hint in TOOL_USAGE_HINTS:
            parts.append(f"- {hint}")

    if include_safety:
        parts.append("\n【安全边界】")
        for boundary in SAFETY_BOUNDARIES:
            parts.append(f"- {boundary}")

    if include_self_awareness:
        parts.append(f"\n【自我认知】{SELF_AWARENESS}")

    # 模式特定提示
    if mode in MODE_PROMPTS:
        mp = MODE_PROMPTS[mode]
        if not compact:
            parts.append(f"\n【当前模式】{mode}")
            for rule in mp["rules"]:
                parts.append(f"- {rule}")
        parts.append(f"\n{mp['suffix']}")

    parts.append(f"\n{OMNI_STACK_POSITIONING}")

    return "\n".join(parts)


