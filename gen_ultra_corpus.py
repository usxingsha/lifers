def generate_entries(knowledge_dict, count_per_item=25):
    """大规模生成条目——每个知识项用多个模板变体"""
    parts = []
    chars = 0
    for domain, entries in knowledge_dict.items():
        parts.append(f"\n\n# {domain}\n")
        for title, body, reflection, practice in entries:
            for i in range(count_per_item):
                tpl = random.choice(TEMPLATES)
                text = tpl.format(title=title, body=body, reflection=reflection, practice=practice)
                parts.append(text)
                chars += len(text)
    return '\n'.join(parts), chars

def generate_cross_domain(count_per_item=15):
    """生成跨领域综合内容"""
    parts = ["\n\n# 跨领域综合与协同\n\n> 真正的洞察往往产生于不同领域的交叉地带。以下内容探讨LIFERS各支柱领域之间的协同关系。\n"]
    chars = 0
    for title, body, reflection in CROSS_DOMAIN:
        for i in range(count_per_item):
            formats = [
                f"\n## {title}\n\n{body}\n\n> {reflection}\n",
                f"\n### 领域交叉: {title}\n\n{body}\n\n*关键洞察: {reflection}*\n",
                f"\n> **交叉分析: {title}**\n> \n> {body}\n> \n> *{reflection}*\n",
            ]
            text = random.choice(formats)
            parts.append(text)
            chars += len(text)
    return '\n'.join(parts), chars

