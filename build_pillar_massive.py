def generate_from_knowledge(knowledge_dict, domain_name):
    """从知识库生成内容，带有多种格式变体"""
    parts = [f"\n\n# {domain_name} 领域知识库\n"]
    chars = 0

    for section_name, items in knowledge_dict.items():
        parts.append(f"\n## {section_name}\n")
        for name, desc in items:
            formats = [
                f"\n### {name}\n\n{desc}\n",
                f"\n**{name}**: {desc}\n\n*这个知识点对于深入理解{domain_name}领域的基础理论和实践应用都具有重要价值。*\n",
                f"\n### 深入理解: {name}\n\n{desc}\n\n> 关键洞察：掌握这一概念需要理论与实践的结合，建议配合实际案例加深理解。\n",
                f"\n# 学习笔记: {name}\n\n{desc}\n\n## 延伸思考\n从这个知识点可以引申出许多相关的应用场景。最重要的是理解其核心思想，而非死记硬背具体的实现细节。\n",
                f"\n> **{name}**\n>\n> {desc}\n",
            ]
            text = random.choice(formats)
            parts.append(text)
            chars += len(text)

    return '\n'.join(parts), chars

def generate_from_list(items, domain_name, count_per_item=8):
    """从知识列表生成内容"""
    parts = [f"\n\n# {domain_name} 知识扩展\n"]
    chars = 0

    for name, desc in items:
        base_formats = [
            f"\n## {name}\n\n{desc}\n",
            f"\n### {name}\n\n{desc}\n\n> 理解这些概念是构建高质量{domain_name}系统的基础。\n",
            f"\n**{name}**\n\n{desc}\n\n*相关阅读：建议进一步了解{domain_name}领域的更多相关内容以加深理解。*\n",
            f"\n# {name}\n\n{desc}\n\n## 实践建议\n\n理论固然重要，但真正的理解来自于实践。建议通过实际项目来巩固这些知识。\n",
        ]
        for _ in range(count_per_item):
            text = random.choice(base_formats)
            parts.append(text)
            chars += len(text)

    return '\n'.join(parts), chars

